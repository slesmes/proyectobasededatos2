package org.models;

public class RedSocial {
    String id;
    String nombre;
    String usuario;
    String idArtista;

    public RedSocial(String id, String nombre, String usuario, String idArtista) {
        this.id = id;
        this.nombre = nombre;
        this.usuario = usuario;
        this.idArtista = idArtista;
    }
}
